package Vue;

public class Point {
	 private  int l;
	    private  int h;

	    public Point(int L, int H) {
	        this.l = L;
	        this.h = H;
	    }

	    public void setL(int L) {
	    	this.l = L;
	    }
	    public void setH(int H) {
	    	this.h = H;
	    }
	    public int getH() {
	        return h;
	    }

	    public int getL() {
	        return l;
	    }


}
